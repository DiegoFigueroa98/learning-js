let myName = 'Diego';
let lastName = 'Figueroa';

// Concatenation
let greeting = 'Hi, my name is ' + myName + ' ' + lastName;

console.log(greeting);

// Interpolation with template String
let secondGreeting = `Hi, my name is ${myName} ${lastName}`;
console.log(secondGreeting);

